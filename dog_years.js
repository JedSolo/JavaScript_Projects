// Assigning a variable to my age:
let myAge = 25;

let earlyYears = 2;
earlyYears *= 10.5;

// Substracting 2 from my age:
let laterYears = myAge - 2;

// Multiplying the later years by 4:
laterYears *= 4;

//Calculating my age in Dog years:
let myAgeInDogYears = earlyYears + laterYears;

// Converting my name to lowercase:
let myName = 'SOLOMON'.toLowerCase();

// Print out my age in dog years:
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);
